public class BringInput {
    private Integer fraPostnummer;
    private Integer tilPostnummer;
    private Integer vektGram;
    private Integer lengdeCm;
    private Integer breddeCm;
    private Integer hoydeCm;

    public BringInput() {
    }

    public Integer getFraPostnummer() {
        return fraPostnummer;
    }

    public void setFraPostnummer(Integer fraPostnummer) {
        this.fraPostnummer = fraPostnummer;
    }

    public Integer getTilPostnummer() {
        return tilPostnummer;
    }

    public void setTilPostnummer(Integer tilPostnummer) {
        this.tilPostnummer = tilPostnummer;
    }

    public Integer getVektGram() {
        return vektGram;
    }

    public void setVektGram(Integer vektGram) {
        this.vektGram = vektGram;
    }

    public Integer getLengdeCm() {
        return lengdeCm;
    }

    public void setLengdeCm(Integer lengdeCm) {
        this.lengdeCm = lengdeCm;
    }

    public Integer getBreddeCm() {
        return breddeCm;
    }

    public void setBreddeCm(Integer breddeCm) {
        this.breddeCm = breddeCm;
    }

    public Integer getHoydeCm() {
        return hoydeCm;
    }

    public void setHoydeCm(Integer hoydeCm) {
        this.hoydeCm = hoydeCm;
    }
}
